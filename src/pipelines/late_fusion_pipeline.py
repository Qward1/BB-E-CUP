#### `LateFusionPipeline`
####- **Назначение**: Главный Late-Fusion pipeline
####- **Методы**:
####  - `initialize_pipelines()`: Инициализация веток
#### - `run_parallel()`: Параллельный запуск
####  - `handle_missing_data()`: Обработка пропусков
#### - `fuse_predictions()`: Объединение предсказаний
#### - `generate_submission()`: Создание файла submission