#### `TextFeatureExtractor`
####- **Назначение**: Извлечение признаков из текста
####- **Методы**:
####  - `extract_statistical_features()`: Длина, количество слов и т.д.
####  - `extract_linguistic_features()`: Лингвистические признаки
####  - `extract_quality_metrics()`: Метрики качества текста
####  - `extract_suspicious_patterns()`: Подозрительные паттерны