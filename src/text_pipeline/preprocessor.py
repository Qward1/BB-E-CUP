#### `TextPreprocessor`
####- **Назначение**: Предобработка текстовых данных
#### - **Методы**:
####  - `clean_html()`: Удаление HTML тегов
####  - `normalize_text()`: Нормализация текста
####  - `handle_missing()`: Обработка отсутствующих описаний
#### - `remove_noise()`: Удаление шума и спецсимволов
#### - `detect_language()`: Определение языка