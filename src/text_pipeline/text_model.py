#### `TextClassifier`
####- **Назначение**: Классификация на основе текста (HuggingFace)
####- **Методы**:
#### - `load_pretrained_model()`: Загрузка модели с HuggingFace
  ####- `fine_tune()`: Fine-tuning на данных
  ####- `predict()`: Получение предсказаний
####- `get_embeddings()`: Извлечение эмбеддингов
  ####- `calculate_confidence()`: Расчет уверенности