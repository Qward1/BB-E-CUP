#### `BrandAnalyzer`
#### - **Назначение**: Анализ упоминаний брендов
#### - **Методы**:
####  - `extract_brands()`: Извлечение брендов из текста
####  - `check_brand_consistency()`: Проверка консистентности
####  - `calculate_brand_similarity()`: Схожесть с оригиналами
####  - `detect_typos()`: Обнаружение опечаток в брендах