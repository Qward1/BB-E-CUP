#### `ConfidenceScorer`
####- **Назначение**: Оценка уверенности предсказаний
####- **Методы**:
####  - `calculate_text_confidence()`: Уверенность text модели
#### - `calculate_metadata_confidence()`: Уверенность meta модели
#### - `calculate_data_completeness()`: Полнота данных
#### - `combine_confidence_scores()`: Комбинированная уверенность