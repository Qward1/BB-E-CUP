#### `WeightOptimizer`
####- **Назначение**: Оптимизация весов для fusion
####- **Методы**:
#### - `optimize_static_weights()`: Статические веса
#### - `optimize_dynamic_weights()`: Динамические веса
#### - `cross_validate_weights()`: Кросс-валидация весов
#### - `calculate_optimal_threshold()`: Оптимальный порог