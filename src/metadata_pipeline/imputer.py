#### `SmartImputer`
#### - **Назначение**: Интеллектуальное заполнение пропусков
#### - **Методы**:
#### - `impute_by_similarity()`: Заполнение по похожим товарам
####  - `impute_by_category()`: Заполнение по категории
####   - `impute_by_brand()`: Заполнение по бренду
####   - `adaptive_imputation()`: Адаптивная стратегия