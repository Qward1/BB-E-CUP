#### `MetadataPreprocessor`
####- **Назначение**: Предобработка метаданных
####- **Методы**:
#### - `process_numerical()`: Обработка числовых признаков
#### - `process_categorical()`: Обработка категориальных
#### - `handle_outliers()`: Обработка выбросов
#### - `create_missing_indicators()`: Индикаторы пропусков