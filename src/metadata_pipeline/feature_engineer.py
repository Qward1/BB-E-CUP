#### `MetadataFeatureEngineer`
#### - **Назначение**: Создание признаков из метаданных
#### - **Методы**:
####  - `create_price_features()`: Признаки на основе цены
####  - `create_seller_features()`: Признаки продавца
####  - `create_category_features()`: Категориальные признаки
####  - `create_interaction_features()`: Взаимодействия признаков
####  - `create_statistical_aggregates()`: Статистические агрегаты